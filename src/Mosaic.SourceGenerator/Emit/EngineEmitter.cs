    /// <summary>
    /// Emits <c>using var __act = MosaicActivitySource.Instance.StartActivity("Mosaic.{kind} {name}")</c>
    /// followed by tag-set calls. No-op if telemetry is disabled in the catalog. Tags use the
    /// well-known keys defined on <c>MosaicActivitySource.Tags</c>.
    /// </summary>
    private static void EmitActivityStart(
        CodeBuilder cb,
        HandlerCatalog catalog,
        string kind,
        string spanSuffix,
        string messageType,
        IReadOnlyList<(string KeyExpr, string ValueExpr)> extraTags)
    {
        if (!catalog.EmitTelemetry) return;

        // Span name: "Mosaic.<Verb> <SimpleName>" — readable in trace UIs without being verbose.
        var verb = kind switch { "request" => "Send", "composable" => "Compose", "event" => "Publish", _ => "Dispatch" };
        cb.AppendLine($"using var __act = global::Mosaic.Runtime.MosaicActivitySource.Instance.StartActivity(\"Mosaic.{verb} {spanSuffix}\");");
        cb.AppendLine($"__act?.SetTag(\"mosaic.message.kind\", \"{kind}\");");
        cb.AppendLine($"__act?.SetTag(\"mosaic.message.type\", \"{Strip(messageType)}\");");
        foreach (var (keyExpr, valueExpr) in extraTags)
        {
            cb.AppendLine($"__act?.SetTag({keyExpr}, {valueExpr});");
        }
    }

    private static string EventPublishModeLiteral(HandlerCatalog catalog)
        => $"global::Mosaic.EventPublishMode.{catalog.EventPublishMode}";

    /// <summary>Removes the leading <c>global::</c> from a fully-qualified type name for cleaner tag values.</summary>
    private static string Strip(string fullyQualified)
        => fullyQualified.StartsWith("global::", StringComparison.Ordinal) ? fullyQualified.Substring(8) : fullyQualified;
}
